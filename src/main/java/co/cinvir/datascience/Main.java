    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Map<String, String> values = new HashMap<>();
        
        values.put("confinan", cbxConfinan.getSelectedItem().toString());
        values.put("conbus", cbxConbus.getSelectedItem().toString());
        values.put("conclerg", cbxConclerg.getSelectedItem().toString());
        values.put("coneduc", cbxConeduc.getSelectedItem().toString());
        values.put("confed", cbxConfed.getSelectedItem().toString());
        values.put("conlabor", cbxConlabor.getSelectedItem().toString());
        values.put("conpress", cbxConpress.getSelectedItem().toString());
        values.put("conmedic", cbxConmedic.getSelectedItem().toString());
        values.put("contv", cbxContv.getSelectedItem().toString());
        values.put("consci", cbxConsci.getSelectedItem().toString());
        values.put("conjudge", cbxConjudge.getSelectedItem().toString());
        values.put("conlegis", cbxConlegis.getSelectedItem().toString());
        values.put("conarmy", cbxConarmy.getSelectedItem().toString());
        values.put("relig", cbxRelig.getSelectedItem().toString());
        
        Optional<ModelProcessor> processorOpt = ModelProcessor.newInstance(values);
        
        ModelProcessor processor = processorOpt.get();
        
        Optional<OutputNode> result = processor.result();
        
        if(result.isPresent()){
            lblResult.setText(result.get().getDecision());
        } else {
            lblResult.setText("ERROR!!!");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

